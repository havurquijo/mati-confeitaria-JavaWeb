package util;

/**
 *
 * @author hvurq
 */
public class PasswObject {
    private int id;
    private String password;
    private String salt;

    public PasswObject(int id, String password, String salt) {
        this.id = id;
        this.password = password;
        this.salt = salt;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getSalt() {
        return salt;
    }

    public void setSalt(String salt) {
        this.salt = salt;
    }
    
    
}
